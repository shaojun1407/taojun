public class SystemDmeo {

	public static void main(String[] args) {
		String st = new String("  AABBCCDDEEFF  ");
		System.out.println(st);
		System.out.println("st:" + st);
		System.out.println("st.length(): " + st.length());
		System.out.println("st.trim(): " + st.trim());
		System.out.println("st.concat(): " + st.concat("GG"));

	}

}
